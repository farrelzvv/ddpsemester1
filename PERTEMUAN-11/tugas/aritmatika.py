#membuat fungsi aritmatika

#Fungsi Penjumlahan (1)
def penjumlahan(bil1, bil2):
    hasil = bil1 + bil2
    print("Hasil dari ", bil1, "ditambah", bil2, "ialah", hasil)

#Fugsi Pengurangan (2)
def pengurangan(bil1, bil2):
    hasil = bil1 - bil2
    print("Hasil dari ", bil1, "dikurang", bil2, "ialah", hasil)

#Fungsi Perkalian (3)
def perkalian(bil1, bil2):
    hasil = bil1 * bil2
    print("Hasil dari ", bil1, "dikali", bil2, "ialah", hasil)

#Fungsi Pembagian (4)
def pembagian(bil1, bil2):
    hasil = bil1 / bil2
    print("Hasil dari ", bil1, "dibagi", bil2, "ialah", hasil)

#Fungsi Hasil Bagi (5)
def hasil_bagi(bil1, bil2):
    hasil = bil1 % bil2
    print("Hasil  dari ", bil1, "modulus", bil2, "ialah", hasil)

#Fungsi Pembagian Bulat (6)
def pembagian_bulat(bil1, bil2):
    hasil = bil1 // bil2
    print("Hasil dari ", bil1, "dibagi", bil2, "ialah", hasil)

#Fungsi perpangakatan (7) 
def perpangkatan(bil1, bil2):
    hasil = bil1 ** bil2 
    print("Hasil dari ", bil1, "pangkat", bil2, "ialah", hasil)

#Fungsi Operasi Gabungan (8)
def operasi_gabungan(bil1, bil2, bil3):
    hasil = bil1 + bil2 * bil3
    print("Hasil dari ", bil1, "ditambah", bil2, "dikali", bil3, "ialah", hasil)

#Fungsi Operasi Gabungan Lanjutan (9)
def operasi_gabungan_lanjutan(bil1, bil2, bil3):
    hasil = (bil1 + bil2) * bil3
    print("Hasil dari ", bil1, "ditambah", bil2, "dikali", bil3, "ialah", hasil)

#Fungsi Operasi perkalian Lanjutan (9)
def operasi_gabungan_lanjutan(bil1, bil2, bil3):
    hasil = bil1 * bil2 * bil3
    print("Hasil dari ", bil1, "dikali", bil2, "dikali", bil3, "ialah", hasil)






    




    

    